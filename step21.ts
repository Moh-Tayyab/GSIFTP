/*logical operator 
AND = && , OR = ||
AND = All condition is true 
OR = if only one condition true return true
*/

let n = 7;
console.log(n >= 7 && n < 10);
console.log(n > 7 && n < 10);
console.log(n >= 7 || n < 10);
console.log(n > 7 || n < 10);
console.log(!(n < 10));
console.log(!(n > 10));
